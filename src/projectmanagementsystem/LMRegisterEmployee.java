    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Utilities utils = new Utilities();

        if (utils.nullChecker(name.getText()) || utils.nullChecker(personNR.getText())
                || utils.nullChecker(userName.getText()) || utils.nullChecker(password.getText())) {
            try {
                utils.insertUserData(name.getText(), personNR.getText(), userName.getText(), password.getText());
            } catch (ClassNotFoundException | SQLException ex) {
            }
            JOptionPane.showMessageDialog(null, name + " har lagts till som anställd");
            this.dispose();
            lm.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Något av fälten är tomt");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

