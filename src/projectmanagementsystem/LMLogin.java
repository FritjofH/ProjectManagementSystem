    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Utilities utils = new Utilities();
        if (utils.nullChecker(jTextField1.getText()) || utils.nullChecker(jTextField2.getText())) {
            try {
                if (utils.loginUser(jTextField1.getText(), jTextField2.getText())) {
                    int id = utils.getUserid(jTextField1.getText(), jTextField2.getText());
                    lm.dispose();
                    this.dispose();
                    MainMenu mm = new MainMenu(id);
                } else {
                    try {
                        if (utils.loginOwner(jTextField1.getText(), jTextField2.getText())) {
                            int projectid = utils.getProjectid(jTextField2.getText());
                            lm.dispose();
                            this.dispose();
                            OwnerFollowup of = new OwnerFollowup(projectid);
                        } else {
                            JOptionPane.showMessageDialog(null, "Felaktigt användarnamn eller lösenord");
                        }

                    } catch (SQLException | ClassNotFoundException | ParseException ex1) {
                        ex1.printStackTrace();
                    }
                }
            } catch (SQLException | ClassNotFoundException ex) {
                ex.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Något av fälten är tomt");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

